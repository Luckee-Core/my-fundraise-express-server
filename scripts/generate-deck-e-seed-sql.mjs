/**
 * Regenerates docs/sql/008_seed_deck_e_pitch_deck.sql from compiled deck-e-slides.
 * Run: npm run build && node scripts/generate-deck-e-seed-sql.mjs
 */
import { writeFileSync } from 'fs';
import { join, dirname } from 'path';
import { fileURLToPath } from 'url';
import {
  DECK_E_PITCH_DECK_ID,
  DECK_E_SLIDE_IDS,
  DECK_E_SLIDE_ORDER,
  DECK_E_SLIDE_TITLES,
  DECK_E_SLIDE_TSX,
} from '../dist/src/utils/business-context/deck-e-slides/index.js';

const root = join(dirname(fileURLToPath(import.meta.url)), '..');
const deckId = DECK_E_PITCH_DECK_ID;

const header = `-- Deck E — On-Prem Install: pitch deck + 9 slides (live review set)
-- Requires: 001_investors_pitch_decks.sql
-- AUTO-GENERATED by scripts/generate-deck-e-seed-sql.mjs
-- Edit src/utils/business-context/deck-e-slides/*.ts then: npm run build && node scripts/generate-deck-e-seed-sql.mjs

ALTER TABLE public.pitch_decks DROP CONSTRAINT IF EXISTS pitch_decks_created_by_user_id_fkey;

ALTER TABLE public.pitch_deck_slides DROP CONSTRAINT IF EXISTS pitch_deck_slides_pitch_deck_id_slide_key_key;
ALTER TABLE public.pitch_deck_slides ADD COLUMN IF NOT EXISTS title VARCHAR(255) NOT NULL DEFAULT 'Untitled slide';

DO $$
BEGIN
  IF EXISTS (
    SELECT 1 FROM information_schema.columns
    WHERE table_schema = 'public' AND table_name = 'pitch_deck_slides' AND column_name = 'slide_key'
  ) THEN
    ALTER TABLE public.pitch_deck_slides RENAME COLUMN slide_key TO template_key;
  END IF;
END $$;

ALTER TABLE public.pitch_deck_slides ALTER COLUMN template_key DROP NOT NULL;
CREATE INDEX IF NOT EXISTS idx_pitch_deck_slides_deck_position ON public.pitch_deck_slides (pitch_deck_id, position);

DELETE FROM public.pitch_deck_slides
WHERE pitch_deck_id = '${deckId}'
  AND id NOT IN (${DECK_E_SLIDE_ORDER.map((key) => `'${DECK_E_SLIDE_IDS[key]}'`).join(', ')});

INSERT INTO public.pitch_decks (id, name, deck_type, version_number, status, created_at, updated_at)
VALUES ('${deckId}', 'Deck E — On-Prem Install', 'intro_screener', 1, 'draft', now(), now())
ON CONFLICT (id) DO UPDATE SET name = EXCLUDED.name, deck_type = EXCLUDED.deck_type, updated_at = now();

INSERT INTO public.pitch_deck_slides (id, pitch_deck_id, title, template_key, content_json, position, created_at, updated_at)
VALUES
`;

const rows = DECK_E_SLIDE_ORDER.map((key, position) => {
  const tag = `$tsx_${key}$`;
  const title = DECK_E_SLIDE_TITLES[key].replace(/'/g, "''");
  return `  ('${DECK_E_SLIDE_IDS[key]}', '${deckId}', '${title}', '${key}', jsonb_build_object('tsx', ${tag}${DECK_E_SLIDE_TSX[key].trim()}${tag}), ${position}, now(), now())`;
});

const sql = `${header}${rows.join(',\n')}
ON CONFLICT (id) DO UPDATE SET
  title = EXCLUDED.title,
  template_key = EXCLUDED.template_key,
  content_json = EXCLUDED.content_json,
  position = EXCLUDED.position,
  updated_at = now();
`;

writeFileSync(join(root, 'docs/sql/008_seed_deck_e_pitch_deck.sql'), sql, 'utf8');
console.log('✅ Wrote docs/sql/008_seed_deck_e_pitch_deck.sql');
