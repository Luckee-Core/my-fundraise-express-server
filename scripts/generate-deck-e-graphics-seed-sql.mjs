/**
 * Regenerates docs/sql/010_seed_deck_e_on_prem_graphics.sql from compiled deck-e-slides.
 * Run: npm run generate:deck-e-graphics-seed
 */
import { writeFileSync } from 'fs';
import { join, dirname } from 'path';
import { fileURLToPath } from 'url';
import {
  DECK_E_GRAPHIC_IDS,
  DECK_E_GRAPHIC_TITLES,
  DECK_E_PITCH_DECK_ID,
  DECK_E_SLIDE_CANVAS_HEIGHT_PX,
  DECK_E_SLIDE_CANVAS_WIDTH_PX,
  DECK_E_SLIDE_ORDER,
  DECK_E_SLIDE_TSX,
} from '../dist/src/utils/business-context/deck-e-slides/index.js';

const root = join(dirname(fileURLToPath(import.meta.url)), '..');
const deckId = DECK_E_PITCH_DECK_ID;

const header = `-- Deck E — On-Prem Install: six Graphics Studio rows + pitch deck links
-- Requires: 003_image_graphics.sql, 008_seed_deck_e_pitch_deck.sql (deck row)
-- AUTO-GENERATED by scripts/generate-deck-e-graphics-seed-sql.mjs
-- Edit src/utils/business-context/deck-e-slides/*.ts then: npm run generate:deck-e-graphics-seed

-- From 005_image_graphics_canvas_columns.sql (older Supabase may lack these columns)
ALTER TABLE public.image_graphics
  ADD COLUMN IF NOT EXISTS canvas_width_px INTEGER,
  ADD COLUMN IF NOT EXISTS canvas_height_px INTEGER;

UPDATE public.image_graphics
SET
  canvas_width_px = COALESCE(canvas_width_px, 1080),
  canvas_height_px = COALESCE(canvas_height_px, 1080)
WHERE canvas_width_px IS NULL OR canvas_height_px IS NULL;

ALTER TABLE public.image_graphics
  ALTER COLUMN canvas_width_px SET DEFAULT 1080,
  ALTER COLUMN canvas_height_px SET DEFAULT 1080;

-- Single-tenant: user_id optional (see 011_drop_image_graphics_user_fk.sql)
ALTER TABLE public.image_graphics
  DROP CONSTRAINT IF EXISTS image_graphics_user_id_fkey;

DO $$
BEGIN
  IF EXISTS (
    SELECT 1 FROM information_schema.columns
    WHERE table_schema = 'public' AND table_name = 'image_graphics' AND column_name = 'user_id'
  ) THEN
    ALTER TABLE public.image_graphics ALTER COLUMN user_id DROP NOT NULL;
  END IF;
END $$;

CREATE TABLE IF NOT EXISTS public.pitch_deck_intro_slide_graphics (
  id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  pitch_deck_id UUID NOT NULL REFERENCES public.pitch_decks (id) ON DELETE CASCADE,
  slide_key TEXT NOT NULL,
  image_graphic_id UUID REFERENCES public.image_graphics (id) ON DELETE SET NULL,
  created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
  updated_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
  UNIQUE (pitch_deck_id, slide_key)
);

CREATE INDEX IF NOT EXISTS idx_pitch_deck_intro_slide_graphics_deck_id
  ON public.pitch_deck_intro_slide_graphics (pitch_deck_id);

INSERT INTO public.image_graphics (id, title, canvas_width_px, canvas_height_px, metadata, created_at, updated_at)
VALUES
`;

const graphicRows = DECK_E_SLIDE_ORDER.map((key) => {
  const tag = `$tsx_${key}$`;
  const title = DECK_E_GRAPHIC_TITLES[key].replace(/'/g, "''");
  return `  ('${DECK_E_GRAPHIC_IDS[key]}', '${title}', ${DECK_E_SLIDE_CANVAS_WIDTH_PX}, ${DECK_E_SLIDE_CANVAS_HEIGHT_PX}, jsonb_build_object('generationStatus', 'complete', 'studioDraft', jsonb_build_object('tsx', ${tag}${DECK_E_SLIDE_TSX[key].trim()}${tag}), 'deckE', jsonb_build_object('templateKey', '${key}', 'pitchDeckId', '${deckId}')), now(), now())`;
});

const junctionHeader = `
ON CONFLICT (id) DO UPDATE SET
  title = EXCLUDED.title,
  canvas_width_px = EXCLUDED.canvas_width_px,
  canvas_height_px = EXCLUDED.canvas_height_px,
  metadata = EXCLUDED.metadata,
  updated_at = now();

DELETE FROM public.pitch_deck_intro_slide_graphics
WHERE pitch_deck_id = '${deckId}'
  AND slide_key NOT IN (${DECK_E_SLIDE_ORDER.map((key) => `'${key}'`).join(', ')});

INSERT INTO public.pitch_deck_intro_slide_graphics (pitch_deck_id, slide_key, image_graphic_id, created_at, updated_at)
VALUES
`;

const junctionRows = DECK_E_SLIDE_ORDER.map(
  (key) =>
    `  ('${deckId}', '${key}', '${DECK_E_GRAPHIC_IDS[key]}', now(), now())`,
);

const junctionFooter = `
ON CONFLICT (pitch_deck_id, slide_key) DO UPDATE SET
  image_graphic_id = EXCLUDED.image_graphic_id,
  updated_at = now();
`;

const sql = `${header}${graphicRows.join(',\n')}${junctionHeader}${junctionRows.join(',\n')}${junctionFooter}`;

writeFileSync(join(root, 'docs/sql/010_seed_deck_e_on_prem_graphics.sql'), sql, 'utf8');
console.log('✅ Wrote docs/sql/010_seed_deck_e_on_prem_graphics.sql');
